function classifyUser(avgTime1, avgErrors1, avgTime2, avgErrors2) {

  // –†–∞—Å—Å—á–∏—Ç—ã–≤–∞–µ–º –ø—Ä–æ–∏–∑–≤–æ–¥–Ω—ã–µ –ø–æ–∫–∞–∑–∞—Ç–µ–ª–∏
  let timeIncrease = (avgTime2 - avgTime1) / avgTime1;
  let errorsIncrease = (avgErrors2 - avgErrors1) / Math.max(1, avgErrors1);

  // –ë–∞–∑–æ–≤—ã–µ –ø–æ—Ä–æ–≥–æ–≤—ã–µ –∑–Ω–∞—á–µ–Ω–∏—è. –ò–• –ù–£–ñ–ù–û –ö–ê–õ–ò–ë–†–ò–†–û–í–ê–¢–¨ –ù–ê –†–ï–ê–õ–¨–ù–´–• –î–ê–ù–ù–´–•!
  const HIGH_TIME_INCREASE = 0.25; // 25% —É–≤–µ–ª–∏—á–µ–Ω–∏–µ –≤—Ä–µ–º–µ–Ω–∏ - –∑–Ω–∞—á–∏–º–æ–µ
  const HIGH_ERRORS_INCREASE = 0.5; // 50% —É–≤–µ–ª–∏—á–µ–Ω–∏–µ –æ—à–∏–±–æ–∫ - –∑–Ω–∞—á–∏–º–æ–µ
  const VERY_HIGH_ABSOLUTE_TIME = 60; // —Å–µ–∫—É–Ω–¥, –æ—á–µ–Ω—å –≤—ã—Å–æ–∫–æ–µ —Å—Ä–µ–¥–Ω–µ–µ –≤—Ä–µ–º—è
  const HIGH_ABSOLUTE_ERRORS = 3; // –≤—ã—Å–æ–∫–æ–µ —Å—Ä–µ–¥–Ω–µ–µ –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ –æ—à–∏–±–æ–∫

  // –ì–†–£–ü–ü–ê 1: "–ë–µ–∑ –Ω–∞—Ä—É—à–µ–Ω–∏–π –∏–Ω–≥–∏–±–∏—Ç–æ—Ä–Ω–æ–≥–æ –∫–æ–Ω—Ç—Ä–æ–ª—è"
  // –•–∞—Ä–∞–∫—Ç–µ—Ä–∏—Å—Ç–∏–∫–∞: –•–æ—Ä–æ—à–∏–µ –ø–æ–∫–∞–∑–∞—Ç–µ–ª–∏ –≤ –±–∞–∑–æ–≤–æ–º —Ç–µ—Å—Ç–µ, –ø–æ–º–µ—Ö–∞ –ø–æ—á—Ç–∏ –Ω–µ –ø–æ–≤–ª–∏—è–ª–∞.
  if (avgTime1 < VERY_HIGH_ABSOLUTE_TIME &&
      avgErrors1 <= HIGH_ABSOLUTE_ERRORS &&
      timeIncrease < HIGH_TIME_INCREASE &&
      errorsIncrease < HIGH_ERRORS_INCREASE) {
    return 0;
  }

  // –ì–†–£–ü–ü–ê 2: "–ù–µ–π—Ä–æ—Ä–∞–∑–Ω–æ–æ–±—Ä–∞–∑–∏–µ –∏ –Ω–∞—Ä—É—à–µ–Ω–∏—è –Ω–µ–π—Ä–æ—Ä–∞–∑–≤–∏—Ç–∏—è (–Ω–∞–ø—Ä–∏–º–µ—Ä, –°–î–í–ì)"
  // –•–∞—Ä–∞–∫—Ç–µ—Ä–∏—Å—Ç–∏–∫–∞: –ú–æ–≥—É—Ç –±—ã—Ç—å –æ—à–∏–±–∫–∏ –≤ –±–∞–∑–æ–≤–æ–º —Ç–µ—Å—Ç–µ, –Ω–æ –ö–õ–Æ–ß–ï–í–û–ô –ø—Ä–∏–∑–Ω–∞–∫ - 
  // –∫–∞—Ç–∞—Å—Ç—Ä–æ—Ñ–∏—á–µ—Å–∫–æ–µ —É—Ö—É–¥—à–µ–Ω–∏–µ –ø—Ä–∏ –Ω–∞–ª–∏—á–∏–∏ –ø–æ–º–µ—Ö–∏ (–æ—Å–æ–±–µ–Ω–Ω–æ –ø–æ –æ—à–∏–±–∫–∞–º).
  else if (errorsIncrease >= HIGH_ERRORS_INCREASE * 1.5) { // –°–∏–ª—å–Ω–æ —Ä–µ–∞–≥–∏—Ä—É—é—Ç –Ω–∞ –ø–æ–º–µ—Ö—É –æ—à–∏–±–∫–∞–º–∏
    return 1;
  }

  // –ì–†–£–ü–ü–ê 3: "–ü—Ä–∏–æ–±—Ä–µ—Ç–µ–Ω–Ω—ã–µ –Ω–µ–≤—Ä–æ–ª–æ–≥–∏—á–µ—Å–∫–∏–µ —Å–æ—Å—Ç–æ—è–Ω–∏—è (–Ω–∞–ø—Ä–∏–º–µ—Ä, –ø–æ—Å–ª–µ –ß–ú–¢, –∏–Ω—Å—É–ª—å—Ç–∞)"
  // –•–∞—Ä–∞–∫—Ç–µ—Ä–∏—Å—Ç–∏–∫–∞: –í—ã—Å–æ–∫–æ–µ –±–∞–∑–æ–≤–æ–µ –≤—Ä–µ–º—è –∏/–∏–ª–∏ –æ—à–∏–±–∫–∏. –ü–æ–º–µ—Ö–∞ —É—Ö—É–¥—à–∞–µ—Ç —Ä–µ–∑—É–ª—å—Ç–∞—Ç,
  // –Ω–æ –Ω–µ —Ç–∞–∫ –¥—Ä–∞–º–∞—Ç–∏—á–Ω–æ, –∫–∞–∫ –≤ –≥—Ä—É–ø–ø–µ 2. –û–±—â–∞—è –º–µ–¥–ª–∏—Ç–µ–ª—å–Ω–æ—Å—Ç—å.
  else if (avgTime1 >= VERY_HIGH_ABSOLUTE_TIME ||
           (avgErrors1 >= HIGH_ABSOLUTE_ERRORS && timeIncrease > HIGH_TIME_INCREASE)) {
    return 2;
  }

  // –ì–†–£–ü–ü–ê 4: "–ü—Å–∏—Ö–∏–∞—Ç—Ä–∏—á–µ—Å–∫–∏–µ —Ä–∞—Å—Å—Ç—Ä–æ–π—Å—Ç–≤–∞ (–Ω–∞–ø—Ä–∏–º–µ—Ä, —Ç—Ä–µ–≤–æ–∂–Ω–æ—Å—Ç—å, –¥–µ–ø—Ä–µ—Å—Å–∏—è)"
  // –•–∞—Ä–∞–∫—Ç–µ—Ä–∏—Å—Ç–∏–∫–∞: –ü–æ–º–µ—Ö–∞ –≤—ã–∑—ã–≤–∞–µ—Ç –∑–Ω–∞—á–∏—Ç–µ–ª—å–Ω–æ–µ —É–≤–µ–ª–∏—á–µ–Ω–∏–µ –í–†–ï–ú–ï–ù–ò (–∑–∞–º–µ–¥–ª–µ–Ω–∏–µ, 
  // –ø–µ—Ä–µ–ø—Ä–æ–≤–µ—Ä–∫–∏), –Ω–æ –Ω–µ –æ–±—è–∑–∞—Ç–µ–ª—å–Ω–æ –∫–æ–ª–∏—á–µ—Å—Ç–≤–∞ –≥—Ä—É–±—ã—Ö –æ—à–∏–±–æ–∫.
  else if (timeIncrease >= HIGH_TIME_INCREASE && errorsIncrease < HIGH_ERRORS_INCREASE) {
    return 3;
  }

  // –ì–†–£–ü–ü–ê 5: "–ü–æ—Å–ª–µ–¥—Å—Ç–≤–∏—è —Å—Ç–∞—Ä–µ–Ω–∏—è –∏ –Ω–µ–π—Ä–æ–¥–µ–≥–µ–Ω–µ—Ä–∞—Ü–∏–∏"
  // –•–∞—Ä–∞–∫—Ç–µ—Ä–∏—Å—Ç–∏–∫–∞: –í—ã—Å–æ–∫–∏–µ –∞–±—Å–æ–ª—é—Ç–Ω—ã–µ –ø–æ–∫–∞–∑–∞—Ç–µ–ª–∏ –∫–∞–∫ –≤ –ø–µ—Ä–≤–æ–º, —Ç–∞–∫ –∏ –≤–æ –≤—Ç–æ—Ä–æ–º —Ç–µ—Å—Ç–µ.
  // –°–∏—Å—Ç–µ–º–Ω–æ–µ —É—Ö—É–¥—à–µ–Ω–∏–µ, –Ω–µ —Ç–∞–∫–æ–µ –∏–∑–±–∏—Ä–∞—Ç–µ–ª—å–Ω–æ–µ –∫ —Ç–∏–ø—É –ø–æ–º–µ—Ö–∏.
  else if (avgTime1 > VERY_HIGH_ABSOLUTE_TIME * 0.8 && // –í—ã—Å–æ–∫–æ–µ –≤—Ä–µ–º—è –¥–∞–∂–µ –±–µ–∑ –ø–æ–º–µ—Ö–∏
           avgTime2 > VERY_HIGH_ABSOLUTE_TIME && // –û—Å—Ç–∞–µ—Ç—Å—è –≤—ã—Å–æ–∫–∏–º —Å –ø–æ–º–µ—Ö–æ–π
           avgErrors1 > HIGH_ABSOLUTE_ERRORS * 0.8) { // –ò —Å—Ç–∞–±–∏–ª—å–Ω–æ –≤—ã—Å–æ–∫–∏–µ –æ—à–∏–±–∫–∏
    return 4;
  }

  // –ï—Å–ª–∏ –Ω–∏ –æ–¥–Ω–∞ –∏–∑ —á–µ—Ç–∫–∏—Ö –≥—Ä—É–ø–ø –Ω–µ –æ–ø—Ä–µ–¥–µ–ª–∏–ª–∞—Å—å
  else {
    return 5;
  }
}
